(function () {
    "use strict";
    const SUPPORTED = ["en", "bn"];

    function getLang() {
        try {
            const v = localStorage.getItem("lang");
            if (v && SUPPORTED.includes(v)) return v;
        } catch (e) {}
        return "en";
    }
    function setLang(lang) {
        if (!SUPPORTED.includes(lang)) return;
        try { localStorage.setItem("lang", lang); } catch (e) {}
    }

    function resolve(lang, key) {
        let node = window.TRANSLATIONS[lang];
        for (const part of key.split(".")) {
            if (node && typeof node === "object" && part in node) node = node[part];
            else return undefined;
        }
        return node;
    }

    function t(key) {
        const lang = getLang();
        const val = resolve(lang, key);
        if (val !== undefined && typeof val === "string") return val;
        const fb = resolve("en", key);
        return typeof fb === "string" ? fb : key;
    }

    function tlist(key) {
        const lang = getLang();
        const val = resolve(lang, key);
        if (Array.isArray(val)) return val;
        const fb = resolve("en", key);
        return Array.isArray(fb) ? fb : [];
    }

    window.i18n = { getLang, setLang, t, tlist, SUPPORTED };
})();
